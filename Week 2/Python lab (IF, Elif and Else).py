print("Towards where should I paint (up, down, left or right)?")
dir = input()

if dir == "up":
    print("I am painting in the upwards direction")
elif dir == "down":
    print("I am painting in the downwards direction")
elif dir == "left":
    print("I am painting in the leftwards direction")
else:
    print("I am painting in the rightward direction")